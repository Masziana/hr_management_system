    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        try {
            // Create Attendance object and populate it
            Attendance a = new Attendance();
            a.setAttendanceID(Integer.parseInt(request.getParameter("attendanceID"))); // convert to int
            a.setEmployeeID(request.getParameter("employeeID"));
            a.setAttendanceDate(request.getParameter("attendanceDate"));
            a.setStatus(request.getParameter("status"));

            // Call DAO to update record
            AttendanceDAO.update(a);

            // Redirect back to attendance view
            response.sendRedirect("viewAttendance.jsp");

        } catch (Exception e) {
            e.printStackTrace();
            response.sendError(HttpServletResponse.SC_INTERNAL_SERVER_ERROR, "Failed to update attendance record.");
        }
    }

